import type { EstimatorPreviewSummary } from '../conversation-meta.types';

export function buildEstimateFollowupReply(input: {
  key:
    | 'why_expensive'
    | 'how_priced'
    | 'materials_included'
    | 'can_be_cheaper'
    | 'inspection_why';
  lastPreview?: EstimatorPreviewSummary | null;
}): string {
  const subtotal =
    typeof input.lastPreview?.subtotal === 'number'
      ? `${input.lastPreview.subtotal.toFixed(2)} EUR`
      : 'ориентировъчната сума';

  switch (input.key) {
    case 'why_expensive':
      return [
        `Сумата ${subtotal} е ориентировъчна цена за труд, не крайна договорна оферта.`,
        'Тя обикновено отразява време за монтаж, подготовка, свързване и отговорност за правилно изпълнение.',
        'При реален оглед може да се окаже както по-ниска, така и по-висока според достъпа, трасето, стените и състоянието на инсталацията.',
      ].join(' ');

    case 'how_priced':
      return [
        `Сумата ${subtotal} е изчислена по ориентировъчен модел според подадените от вас данни.`,
        'Моделът използва тип работа, брой елементи и при нужда дължина на трасета или допълнителни условия.',
        'Това е предварителна сметка, а не окончателна оферта на място.',
      ].join(' ');

    case 'materials_included':
      return [
        'Не, материалите не са включени, освен ако изрично не е казано друго.',
        `Показаната сума ${subtotal} е ориентировъчно за труд.`,
      ].join(' ');

    case 'can_be_cheaper':
      return [
        'Възможно е, но това зависи от реалните условия на обекта.',
        `Сумата ${subtotal} е предварителна и може да се коригира след уточняване на детайлите.`,
        'Най-сигурният начин да избегнете излишен разход е оглед и ясно описание на обхвата.',
      ].join(' ');

    case 'inspection_why':
      return [
        'Огледът е нужен, защото по снимки или кратък текст не се виждат всички условия.',
        'На място се проверяват трасета, достъп, стени, състояние на съществуващата инсталация и нужните защити.',
        'Това позволява да се даде реална, а не подвеждаща цена.',
      ].join(' ');

    default:
      return 'Това е ориентировъчна сметка. За точна оферта са нужни още детайли или оглед.';
  }
}
